<h4>Cara Barter di Dapatbuku</h4>
<ol>

<li>
<strong>Cari buku</strong><br>
Kamu bisa mencari buku melalui fitur "Search" atau melihat pada kategori yang telah kami sediakan
</li>

<li>
<strong>Klik Buku</strong><br>
Disini kamu bisa melihat informasi mengenai buku, mulai dari halaman, hingga review buku, dan dimana bisa mendapatkan buku tersebut
</li>

<li>
<strong>Klik Penjual</strong><br>
Jika ada pengguna yang mau menukarkan buku tersebut, kamu bisa mengklik untuk melihat halaman iklan yang dia tampilkan.
</li>

<li>
<strong>Hubungi Penjual</strong><br>
	Disini kamu akan menawarkan buku apa yang ingin kamu tukarkan dengan buku milik si Penjual. Jika dia mau menukarkan bukunya dengan bukumu maka kamubisa menetapkan, bagaimana kamu ingin mendapatkan buku tersebut. Bisa melalui pengiriman ataupun kamu bertemu dengan penjual. <br>
	Kami sangat menyarankan untuk bertemu dengan penjual untuk transaksi barter. Supaya kedua belah pihak dapat melihat kondisi buku dan tidak terjadi kesalahan.
</li>

<li>
<strong>Dapat Bukunya</strong><br>
	Setelah deal dengan penjual kamu bisa dapetin buku yang kamu inginkan.
</li>

<li>
<strong>Baca Bukunya & kasih review</strong><br>
	Baca bukunya sampe habis, jangan lupa kasih review tentang buku tersebut di Dapatbuku, agar yang lain juga tertarik membaca buku yang kamu baca
</li>
</ol>

<hr>

<h4>Cara Barter di Dapatbuku (Penyedia)</h4>
<ol>


<li>
<strong>Klik jual buku</strong><br>
	Isi form yang terdapat pada menu jual buku, jika kamu bersedia mem barterkan bukumu silahkan centang kolom barter sesuai harga yang kamu inginkan.
</li>

<li>
<strong>Validasi</strong><br>
	Kami akan memvalidasikan iklan yang kamu iklankan dalam waktu 1x24 jam. Kami berhak menolak iklanmu jika kamu melanggar aturan yang berlaku di Dapatbuku
</li>

<li>
<strong>Dapat Pembeli</strong><br>
	Jika pembeli ada yang menghubungi kamu, jangan lupa tanyakan buku apa yang dimiliki dia. Jika sudah deal kami sangat sarankan kamu bertemu dengan
	pengguna yang ingin menukarkan bukunya dengan kamu, agar tidak terjadi penipuan.
</li>

<li>
<strong>Bukunya telah tertukar</strong><br>
	Setelah deal dengan penukar kamu bisa dapetin buku yang kamu sepakati.
</li>

<li>
<strong>Baca Bukunya & kasih review</strong><br>
	Baca bukunya sampe habis, jangan lupa kasih review tentang buku tersebut di Dapatbuku, agar yang lain juga tertarik membaca buku yang kamu baca
</li>
</ol>

