<h3>BookFinder</h3>

<strong>Bagaimana jika buku yang saya inginkan tidak tersedia?</strong>
<p>- Kamu bisa mengisi form RequestBook kami akan memproses permintaan kamu 3x24 jam. Dan kami akan bantu kamu untuk mendapatkan buku yang kamu inginkan.</p>

<strong>Saya berasal diluar Surabaya, apakah bisa saya membarter atau rental lewat Dapatbuku?</strong>
<p>- Saat ini kami hanya melayani rental dan barter di daerah Surabaya. Namun untuk kedepannya tidak menutup kemungkinan bahwa kami akan melebarkan sayap kami ke kota lain.</p>

<strong>Bagaimana jika saya terlambat mengembalikan buku?</strong>
<p>- Umumnya, kamu akan dikenakan biaya seharga sewa buku seperti biasa, atau kamu bisa membuat kesepakatan dengan pihak penyedia buku sebelumnya</p>

<strong>Apa jaminan saya untuk meminjam buku?</strong>
<p>- Uang seharga buku yang kamu pinjam sebagai jaminan. Tetapi kamu juga bisa negosiasi dengan si penjual untuk memilih alternatif jaminan untuk meminjam buku, misal jaminan kartu identitas. Sangat disarankan untuk mengecek buku yang kamu pinjam pertama kali agar tidak terjadi kesalahpahaman dengan penyedia buku.</p>

<hr>

<h3>BookKeeper</h3>

<strong>Apa jaminan yang diberikan Dapatbuku jika buku hilang/rusak?</strong>
<p>- Dapatbuku menyarankan kamu meminta jaminan berupa uang seharga buku, tapi kamu juga bisa meminta jaminan berupa kartu identitas kepada peminjam. Dapatbuku menyerahkan keputusan apakah bukuakan dipinjamkan atau tidak kepada penyedia buku.</p>

<strong>Bagaimana pembagian hasil transaksi di Dapatbuku?</strong>
<p>- Saat ini, Dapatbuku tidak mengambil biaya apapun dari transaksi yang terjadi melalui Dapatbuku</p>



