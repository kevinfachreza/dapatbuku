<h4>Cara Beli di Dapatbuku (Pembeli)</h4>
<ol>
	<li><strong>Cari buku</strong><br>
	Kamu bisa mencari buku melalui fitur "Search" atau melihat pada kategori yang telah disediakan</li>

	<li><strong>Klik Buku</strong><br>
	Disini kamu bisa melihat informasi mengenai buku, mulai dari halaman, hingga review buku, dan dimana bisa mendapatkan buku tersebut
	</li>

	<li><strong>Klik Penjual</strong><br>
	Jika ada pengguna yang menjual buku tersebut, kamu bisa mengklik untuk melihat halaman iklan yang dia tampilkan.
	</li>

	<li><strong>Hubungi Penjual</strong><br>
	Disini kamu bisa menetapkan, bagaimana kamu ingin mendapatkan buku tersebut. Bisa melalui pengiriman ataupun kamu bertemu dengan penjual.
	</li>

	<li><strong>Dapat Bukunya</strong><br>
	Setelah deal dengan penjual, kamu akan mendapatkan buku yang kamu inginkan.
	</li>

	<li><strong>Baca Bukunya & kasih review</strong><br>
	Baca bukunya sampe habis, jangan lupa kasih review tentang buku tersebut di Dapatbuku, agar yang lain juga tertarik membaca buku yang kamu baca
	</li>

</ol>

<hr>

<h4>Cara Jual di Dapatbuku (Penjual)</h4>
<ol>
<li><strong>Klik jual buku</strong><br>
Isi form yang terdapat pada menu jual buku, jika kamu ingin menjual bukumu silahkan isi kolom harga jual sesuai harga yang kamu inginkan</li>

<li><strong>Validasi</strong><br>
Kami akan memvalidasikan iklan yang kamu iklankan dalam waktu 1x24 jam. Kami berhak menolak iklanmu jika kamu melanggar aturan yang berlaku di Dapatbuku</li>


<li><strong>Dapat Pembeli</strong><br>
Jika pembeli ada yang menghubungi kamu, silahkan jual buku kamu sesuai kesepakatan kamu dengan si pembeli. </li>

<li><strong>Buku Terjual</strong><br>
Setelah buku terjual, kamu bisa mengedit stok yang terdapat pada menu 'Edit Buku' atau hapus jika memang kamu ingin menghapus iklan tersebut.</li>
</ol>






