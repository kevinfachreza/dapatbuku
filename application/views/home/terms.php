<h4>Transaksi Umum</h4>
<ol>
<li>Dapatbuku tidak bertanggung jawab atas kebenaran informasi, gambar, dan keterangan pada buku yang dijual, yang dijual selain oleh akun OfficialDapatbuku.</li>
<li>Dapatbuku tidak bertanggung jawab atas penyerahan buku, yang dijual selain oleh akun OfficialDapatbuku.</li>
<li>Pengguna wajib memastikan bahwa buku yang akan ditransaksikan merupakan buku asli dan legal karena Dapatbuku tidak akan memvalidasi buku yang ilegal.</li>
<li>Pembeli memahami dan menyetujui bahwa kesalahan keterangan harga dan informasi lainnya yang disebabkan tidak terbaharuinya halaman situs Dapatbuku dikarenakan browser/ISP yang dipakai Pembeli adalah tanggung jawab Pembeli.</li>
<li>Dapatbuku memiliki kewenangan untuk mengambil keputusan atas permasalahan yang terjadi pada setiap transaksi.</li>
<li>Dapatbuku tidak terlibat dalam proses transaksi, baik secara pembayaran ataupun pengiriman buku selain yang dilakukan oleh OfficialDapatbuku</li>
<li>Dapatbuku tidak memungut biaya untuk transaksi yang terjadi.</li>
</ol>

<hr>

<h4>Jual-Beli</h4>
<ol>
<li>Jual-beli dapat dilakukan jika penyedia buku memberikan harga jual untuk buku yang diunggah.</li>
<li>Jika penyedia buku tidak memberikan harga jual untuk buku yang diunggah, maka Dapatbuku menetapkan buku tidak dijual.</li>
</ol>

<hr>

<h4>Rental</h4>
<ol>
<li>Rental dapat dilakukan jika penyedia buku memberikan harga rental untuk buku yang diupload.</li>
<li>Jika penyedia buku tidak memberikan harga rental untuk buku yang diupload , maka Dapatbuku menetapkan buku tidak dirental.</li>
<li>Harga rental ditetapkan untuk satuan minggu.</li>
<li>Jika penyewa terlambat mengembalikan buku, maka penyedia buku berhak untuk menetapkan denda keterlambatan sama seperti harga rental.</li>
</ol>

<hr>

<h4>Barter</h4>
<ol>
<li>Barter dapat dilakukan jika penyedia buku menetapkan buku untuk dibarterkan.</li>
<li>Dapatbuku tidak terlibat dalam proses barter.</li>
<li>Sangat disarankan barter dilakukan secara bertemu.</li>
</ol>

