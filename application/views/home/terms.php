<h4>Umum</h4>
<ol>
	<li>Dapatbuku merupakan website yang menyediakan informasi buku-buku dan juga portal yang melayani transaksi jual-beli buku.</li>
	<li>Dapatbuku sebagai penjual buku di web portal Dapabuku, selain itu Dapatbuku juga sebagai perantara antara penjual dan pembeli.</li>
</ol>

