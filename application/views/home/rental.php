<h4>Cara Rental di Dapabuku (Perental)</h4>
<ol>

<li><strong>Cari buku</strong><br>
Kamu bisa mencari buku melalui fitur "Search" atau melihat pada kategori yang telah kami sediakan
</li>

<li><strong>
Klik Buku</strong><br>
Disini kamu bisa melihat informasi mengenai buku, mulai dari halaman, hingga review buku, dan dimana bisa mendapatkan buku tersebut
</li>


<li><strong>Klik Penjual</strong><br>
Jika ada pengguna yang merentalkan buku tersebut, kamu bisa mengklik untuk melihat halaman iklan yang dia tampilkan.
</li>


<li><strong>Hubungi Penjual</strong><br>
Disini kamu bisa menetapkan, bagaimana kamu ingin mendapatkan buku tersebut. Bisa melalui pengiriman ataupun kamu bertemu dengan penjual. Dan durasi peminjaman
</li>

<li><strong>Dapat Bukunya</strong><br>
Setelah deal dengan penjual, bayar sesuai harga buku + harga sewa buku sesuai dengan durasi. Kenapa kok bayar harga buku juga? Karena sebagai jaminan, jika terjadi kerusakan atau kehilangan buku.
</li>

<li><strong>Baca Bukunya & kasih review</strong><br>
Baca bukunya sampe habis, jangan lupa kasih review tentang buku tersebut di Dapatbuku, agar yang lain juga tertarik membaca buku yang kamu baca
</li>

<li><strong>Kembalikan buku</strong><br>
Setelah kamu kembalikan buku, maka penjual wajib mengembalikan harga. Jika kamu terlambat, maka kamu akan membayar denda seharga sewa buku.</li>

</ol>
<hr>


<h4>Cara Rental di Dapatbuku (Penyedia)</h4>

<ol>

<li><strong>Klik jual buku</strong><br>
Isi form yang terdapat pada menu jual buku, jika kamu ingin merentalkan bukumu silahkan isi kolom harga rental sesuai harga yang kamu inginkan.</strong><br>
Ingat, harga rental adalah harga perminggu.
</li>

<li><strong>Validasi</strong><br>
Kami akan memvalidasikan iklan yang kamu iklankan dalam waktu 1x24 jam. Kami berhak menolak iklanmu jika kamu melanggar aturan yang berlaku di Dapatbuku</li>

<li><strong>Dapat Pembeli</strong><br>
Jika pembeli ada yang menghubungi kamu, silahkan rentalkan buku kamu sesuai kesepakatan kamu dengan si pembeli. </strong><br>
Sebagai jaminan, kami sarankan pembeli membayar seharga harga buku + harga total rental. Harga buku sebagai jaminan, jika bukumu rusak atau hilang.</li>

<li><strong>Buku Tersewa</strong><br>
Saat proses menyewa, jangan lupa untuk mengingatkan si penyewa soal waktu pengembalian buku.</li>

<li><strong>Buku dikembalikan</strong><br>
Ketika buku kembali, kamu cek dulu apakah buku masih dalam keadaan baik atau sudah dalam keadaan yang tidak karuan.
Kamu berhak memberikan denda kepada perental jika kamu merasa buku yang dirental terjadi kerusakan.
Setelah itu, kamu wajib mengembalikan uang jaminan yang diberikan perental kepada kamu.
</li>





